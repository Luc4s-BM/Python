import time
limite = 10
valor = 0
while valor <= limite:
    print(f"Valor da variável: {valor}")
    time.sleep(1)
    valor += 1
"""O "while" foi configurado para repetir o código enquanto o "valor", for menor que o  "limite", quando 
o "valor" for igual ao "limite" o código se encerra, pois, a condição do "while" não é mais verdadeira.""" 